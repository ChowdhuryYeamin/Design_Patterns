public class ShortOrderCook {
    public void cookBurger() {
        System.out.println("Cooking burger...");
    }

    public void makeShake() {
        System.out.println("Making a shake...");
    }

    public void undoCookBurger() {
        System.out.println("Undo cooking burger...");
    }

    public void undoMakeShake() {
        System.out.println("Undo making a shake...");
    }
}
